package com.seec.insurance.plm.customerinquiry.model;

public class Address {

	String line1;
	String line2;
	String city;
	String state;
	String country;
	String zip;
	String addressKey;
	String isPreferedAddr;

	/**
	 * @return the line1
	 */
	public String getLine1() {
		return line1;
	}
	/**
	 * @param line1 the line1 to set
	 */
	public void setLine1(String line1) {
		this.line1 = line1;
	}
	/**
	 * @return the line2
	 */
	public String getLine2() {
		return line2;
	}
	/**
	 * @param line2 the line2 to set
	 */
	public void setLine2(String line2) {
		this.line2 = line2;
	}
	/**
	 * @return the city
	 */
	public String getCity() {
		return city;
	}
	/**
	 * @param city the city to set
	 */
	public void setCity(String city) {
		this.city = city;
	}
	/**
	 * @return the state
	 */
	public String getState() {
		return state;
	}
	/**
	 * @param state the state to set
	 */
	public void setState(String state) {
		this.state = state;
	}
	/**
	 * @return the country
	 */
	public String getCountry() {
		return country;
	}
	/**
	 * @param country the country to set
	 */
	public void setCountry(String country) {
		this.country = country;
	}
	/**
	 * @return the zip
	 */
	public String getZip() {
		return zip;
	}
	/**
	 * @param zip the zip to set
	 */
	public void setZip(String zip) {
		this.zip = zip;
	}
	/**
	 * @return the addressKey
	 */
	public String getAddressKey() {
		return addressKey;
	}
	/**
	 * @param addressKey the addressKey to set
	 */
	public void setAddressKey(String addressKey) {
		this.addressKey = addressKey;
	}
	/**
	 * @return the isPreferedAddr
	 */
	public String getIsPreferedAddr() {
		return isPreferedAddr;
	}
	/**
	 * @param isPreferedAddr the isPreferedAddr to set
	 */
	public void setIsPreferedAddr(String isPreferedAddr) {
		this.isPreferedAddr = isPreferedAddr;
	}
	
}
