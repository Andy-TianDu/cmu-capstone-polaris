package com.seec.insurance.plm.customerinquiry.model;

public class EmailAddress {
	
	String emailAddressKey;
	String emailType;
	String emailAddress;
	String isPreferedEmail;
	
	/**
	 * @return the emailAddressKey
	 */
	public String getEmailAddressKey() {
		return emailAddressKey;
	}
	/**
	 * @param emailAddressKey the emailAddressKey to set
	 */
	public void setEmailAddressKey(String emailAddressKey) {
		this.emailAddressKey = emailAddressKey;
	}
	/**
	 * @return the emailType
	 */
	public String getEmailType() {
		return emailType;
	}
	/**
	 * @param emailType the emailType to set
	 */
	public void setEmailType(String emailType) {
		this.emailType = emailType;
	}
	/**
	 * @return the emailAddress
	 */
	public String getEmailAddress() {
		return emailAddress;
	}
	/**
	 * @param emailAddress the emailAddress to set
	 */
	public void setEmailAddress(String emailAddress) {
		this.emailAddress = emailAddress;
	}
	/**
	 * @return the isPreferedEmail
	 */
	public String getIsPreferedEmail() {
		return isPreferedEmail;
	}
	/**
	 * @param isPreferedEmail the isPreferedEmail to set
	 */
	public void setIsPreferedEmail(String isPreferedEmail) {
		this.isPreferedEmail = isPreferedEmail;
	}
	
}
