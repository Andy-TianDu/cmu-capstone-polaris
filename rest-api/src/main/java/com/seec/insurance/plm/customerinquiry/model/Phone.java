package com.seec.insurance.plm.customerinquiry.model;

public class Phone {

	String countryCode;
	String areaCode;
	String dialNumber;
	String phoneKey;
	String isPreferedPhone;

	/**
	 * @return the countryCode
	 */
	public String getCountryCode() {
		return countryCode;
	}
	/**
	 * @param countryCode the countryCode to set
	 */
	public void setCountryCode(String countryCode) {
		this.countryCode = countryCode;
	}
	/**
	 * @return the areaCode
	 */
	public String getAreaCode() {
		return areaCode;
	}
	/**
	 * @param areaCode the areaCode to set
	 */
	public void setAreaCode(String areaCode) {
		this.areaCode = areaCode;
	}
	/**
	 * @return the dialNumber
	 */
	public String getDialNumber() {
		return dialNumber;
	}
	/**
	 * @param dialNumber the dialNumber to set
	 */
	public void setDialNumber(String dialNumber) {
		this.dialNumber = dialNumber;
	}
	/**
	 * @return the phoneKey
	 */
	public String getPhoneKey() {
		return phoneKey;
	}
	/**
	 * @param phoneKey the phoneKey to set
	 */
	public void setPhoneKey(String phoneKey) {
		this.phoneKey = phoneKey;
	}
	/**
	 * @return the isPreferedPhone
	 */
	public String getIsPreferedPhone() {
		return isPreferedPhone;
	}
	/**
	 * @param isPreferedPhone the isPreferedPhone to set
	 */
	public void setIsPreferedPhone(String isPreferedPhone) {
		this.isPreferedPhone = isPreferedPhone;
	}
	
}
