package com.seec.insurance.plm.customersearch.model;

public class CustomerSearchResult {

	String partyKey;
	String fullname;
	String leadStage;
	String primaryAddress;
	String primaryPhone;
	String ssn;
	
	/**
	 * @return the partyKey
	 */
	public String getPartyKey() {
		return partyKey;
	}
	/**
	 * @param partyKey the partyKey to set
	 */
	public void setPartyKey(String partyKey) {
		this.partyKey = partyKey;
	}
	/**
	 * @return the fullname
	 */
	public String getFullname() {
		return fullname;
	}
	/**
	 * @param fullname the fullname to set
	 */
	public void setFullname(String fullname) {
		this.fullname = fullname;
	}
	/**
	 * @return the leadStage
	 */
	public String getLeadStage() {
		return leadStage;
	}
	/**
	 * @param leadStage the leadStage to set
	 */
	public void setLeadStage(String leadStage) {
		this.leadStage = leadStage;
	}
	/**
	 * @return the primaryAddress
	 */
	public String getPrimaryAddress() {
		return primaryAddress;
	}
	/**
	 * @param primaryAddress the primaryAddress to set
	 */
	public void setPrimaryAddress(String primaryAddress) {
		this.primaryAddress = primaryAddress;
	}
	/**
	 * @return the primaryPhone
	 */
	public String getPrimaryPhone() {
		return primaryPhone;
	}
	/**
	 * @param primaryPhone the primaryPhone to set
	 */
	public void setPrimaryPhone(String primaryPhone) {
		this.primaryPhone = primaryPhone;
	}
	/**
	 * @return the ssn
	 */
	public String getSsn() {
		return ssn;
	}
	/**
	 * @param ssn the ssn to set
	 */
	public void setSsn(String ssn) {
		this.ssn = ssn;
	}

	
}
